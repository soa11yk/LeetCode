from itertools import combinations

  

class Solution:
    
    
    def threeSumClosest(self, nums: List[int], target: int) -> int:
        nums = sorted(nums)
        length = len(nums)
        theClosest = sum(nums[:3])
        theClosestDist = abs(theClosest-target)
        
        for theLowest in range(length-2):
            theLeft = theLowest + 1
            theRight = length - 1
            while theLeft < theRight:
                threeSum = sum([nums[theLowest], nums[theLeft], nums[theRight]])
                if  abs(threeSum-target) < theClosestDist:
                    theClosest = threeSum
                    theClosestDist = abs(threeSum-target)
                if threeSum == target:
                    return target
                elif threeSum < target:
                    theLeft = theLeft + 1
                elif threeSum > target:
                    theRight = theRight - 1
        return theClosest

                        

